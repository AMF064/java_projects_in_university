class PlayWithAValue {
    public static void main(String[] args) {
        int varInt;                     //Basura
        varInt = 0;                     //0
        varInt += 5;                    //5
        varInt -= 2;                    //3
        varInt *= 3;                    //9
        varInt /= 2;                    //4
        System.out.println(varInt);
    }
}